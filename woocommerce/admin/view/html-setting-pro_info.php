<h2>專業版資訊</h2>
<p>專業版將提供更多方便店家管理賣場的功能。</p>
<p>更完整的說明請至 <a href="https://richer.tw/ry-woocommerce-tools-pro/" target="_blank">RY WooCommerce Tools Pro</a> 觀看。</p>
<p>提供的功能計有</p>
<ol>
	<li>前台的訂單詳細資訊頁面中顯示超商取貨的相關資訊。</li>
	<li>當運送方式為超商取貨的時候，可選擇移除結帳畫面當中訂購人資訊中的地址欄位。</li>
	<li>信用卡分期，將每一個期數設定為一個付款方式。</li>
	<li>於訂單保留的信件當中加入ATM/超商條碼/超商代碼的付款相關資訊。</li>
	<li>綠界超商取貨 B2C 模式。</li>
	<li>綠界超商取貨離島運費加成。</li>
	<li>綠界超商取貨後台支援選擇超商模式。</li>
	<li>綠界超商取貨批次列印託運單(C2C 模式下 7-11 不支援)。</li>
</ol>
